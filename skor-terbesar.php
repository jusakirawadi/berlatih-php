function skor_terbesar($arr){
//kode di sini

    //urutkan nilai tertinggi
    uasort($arr, function($a,$b){
        return strcmp($b['nilai'], $a['nilai']);
    });

    //simpan ke hasil
    $hasil = array();
    foreach ($arr as $key => $val) {
        $temp = $arr[$key];
        $hasil += array($temp['kelas']=>$temp);
    }
  
    return $hasil;
}

